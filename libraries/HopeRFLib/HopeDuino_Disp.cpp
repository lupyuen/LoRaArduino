void dispClass::vDispSetDot(byte x_axis, byte y_axis)
{
 byte i, j;
 i = y_axis&0x07;
 for(j=0x01;i!=0;i--)
 	j <<= 1;
 i = (y_axis>>3)+1;
 JLX12864.vLcdLocate(i, x_axis);
 JLX12864.vSendData(j);
 SetLCS();	
}

