/**********************************************************
**Name:     vDisArduinoLogo
**Function: Show Arduino Logo
**Input:    positive for show positive or inverse
**Output:   none
**********************************************************/
void dispClass::vDisArduinoLogo(bool positive)
{
	byte i, j;
	for(i=1; i<9; i++)
		{
		JLX12864.vLcdLocate(i, 1);
		for(j=0;j<128;j++)
 			{
 			if(positive)
 				JLX12864.vSendData(Image_grap[(((word)(i-1))<<7)+j]^0xFF);	
 			else	
 				JLX12864.vSendData(Image_grap[(((word)(i-1))<<7)+j]);
 			}
		SetLCS();
		}
}

/**********************************************************
**Name:     vDisHopeRFLogo
**Function: Show HopeRF Logo
**Input:    line for start address(1-8), 
            positive for show positive or inverse
**Output:   none
**********************************************************/
void dispClass::vDisHopeRFLogo(byte line, bool positive)
{
	byte i, j;
	if((line==0)||(line>=9))
		line = 1;
	for(i=line; i<(line+4); i++)
		{
		JLX12864.vLcdLocate(i, 1);
		for(j=0;j<128;j++)
 			{
 			if(positive)
 				JLX12864.vSendData(logo_grap[(((word)(i-line))<<7)+j]^0xFF);	
 			else	
 				JLX12864.vSendData(logo_grap[(((word)(i-line))<<7)+j]);
 			}
		SetLCS();
		}
}	
	
/**********************************************************
**Name:     vDispGrap32x32
**Function: Show 32*32 grap.
**Input:    page, column, ptr
**Output:   none
**********************************************************/
void dispClass::vDispGrap32x32(byte page, byte column, byte ptr[])		
{
	byte j;
	for(j=0; j<4; j++)
		{
		JLX12864.vLcdLocate(page+j, column);
		JLX12864.vBurstSendData(ptr, 32);
		}
}

